    private void searchBarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchBarKeyReleased
        DefaultTableModel tb1Model = (DefaultTableModel) productsTable.getModel();
        String search = searchBar.getText().toLowerCase();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(tb1Model);
        productsTable.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
    }//GEN-LAST:event_searchBarKeyReleased

    private void productsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productsTableMouseClicked
      
        DefaultTableModel tb1Model = (DefaultTableModel) productsTable.getModel();
        String tbId = tb1Model.getValueAt(productsTable.getSelectedRow(),0).toString();
        String tbName = tb1Model.getValueAt(productsTable.getSelectedRow(),1).toString();
        
        String tbPrice = tb1Model.getValueAt(productsTable.getSelectedRow(),2).toString();
        String tbOfferPrice = tb1Model.getValueAt(productsTable.getSelectedRow(),3).toString();
         proId.setText(tbId);
        proName.setText(tbName);
        
        // set at textField
     
        proPrice.setText(tbPrice);
        if(!tbOfferPrice.toLowerCase().equals("no offer")){
           proPrice.setText(tbOfferPrice);
        }
    }//GEN-LAST:event_productsTableMouseClicked
     
