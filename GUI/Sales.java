    private void addProOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addProOrderActionPerformed
            try{
  

    Integer.parseInt(proPrice.getText());
       }
   catch(Exception  message){
       
         JOptionPane.showMessageDialog(this, "Enter a valid value in Text Field", "Error", JOptionPane.INFORMATION_MESSAGE);
         return;
       }
         
            if(proId.getText().equals("")||proName.getText().equals("")||proPrice.getText().equals("")){
            JOptionPane.showMessageDialog(this, "please Enter All Data !");

        }else{
            String data[] = {proId.getText(),proName.getText(),proPrice.getText()};
            DefaultTableModel tb1Model = (DefaultTableModel) orderTable.getModel();
            tb1Model.addRow(data);
            JOptionPane.showMessageDialog(this, "successfully added");
            proId.setText("");
            proName.setText("");
            proPrice.setText("");
         
                    
                    
        }
        
    }//GEN-LAST:event_addProOrderActionPerformed

